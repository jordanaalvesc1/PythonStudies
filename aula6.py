# conversão de tipos, coerção
# type converstion, typecasting coercion
# é o ato de converter um tipo em outro
# tipos inmutáveis e primitivos:
# str, int, float, bool
print(1+1)
print('a' + 'b')
# print('1' + 1) da erro pois só pode concatenar tipo com tipo
print(int('1'), type('1'))
print (float('1') + 1)
print (bool(' '))
print(str(11)+'b')